package kr.ngii.pilot.sdmc.main.service.vo;

public class DoyeupVO {

	private String doyeupId = null;
	private String doyeupName = null;
	private String doyeupNameEng = null;
	private String geom = null; 
	private String regDt = null;
	private String regUserId = null;
	
	public String getDoyeupId() {
		return doyeupId;
	}
	public void setDoyeupId(String doyeupId) {
		this.doyeupId = doyeupId;
	}
	public String getDoyeupName() {
		return doyeupName;
	}
	public void setDoyeupName(String doyeupName) {
		this.doyeupName = doyeupName;
	}
	public String getDoyeupNameEng() {
		return doyeupNameEng;
	}
	public void setDoyeupNameEng(String doyeupNameEng) {
		this.doyeupNameEng = doyeupNameEng;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getRegUserId() {
		return regUserId;
	}
	public void setRegUserId(String regUserId) {
		this.regUserId = regUserId;
	}
	public String getGeom() {
		return geom;
	}
	public void setGeom(String geom) {
		this.geom = geom;
	}
	
		
}
